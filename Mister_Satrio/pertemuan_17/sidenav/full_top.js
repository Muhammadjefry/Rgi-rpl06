function openNav() {
  document.getElementById("mySidenav").style.minHeight = "100%";
  document.getElementById("mySidenav").style.bottom = "0";
  document.getElementById("mySidenav").style.marginBottom = "0";
}
function closeNav() {
  document.getElementById("mySidenav").style.minheight = "0";
  document.getElementById("mySidenav").style.bottom = "100%";
  document.getElementById("mySidenav").style.marginBottom = "0";
}
