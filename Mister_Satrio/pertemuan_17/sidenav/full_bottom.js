function openNav() {
  document.getElementById("mySidenav").style.minHeight = "100%";
  document.getElementById("mySidenav").style.top = "0";
  document.getElementById("mySidenav").style.marginTop = "0";
}
function closeNav() {
  document.getElementById("mySidenav").style.minheight = "0";
  document.getElementById("mySidenav").style.top = "100%";
  document.getElementById("mySidenav").style.marginTop = "0";
}
